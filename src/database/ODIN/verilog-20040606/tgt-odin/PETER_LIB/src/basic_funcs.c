void *paj_alloc(int size)
{
	void *allocation;

	if (size <= 0)
	{
		printf("Allocation size <= 0\n");
		trapAssert(FALSE);
	}

//	allocation = calloc(size, 0);
	allocation = malloc(size);

	if (allocation == NULL)
	{
		printf("Allocation FAILED\n");
		trapAssert(FALSE);
	}

	return allocation;
}
